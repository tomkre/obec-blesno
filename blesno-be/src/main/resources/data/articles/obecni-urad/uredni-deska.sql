<h3>&nbsp;Výměna řidičských průkazů finišuje, už zbývá jen 900 tisíc</h3>
<p><strong>PRAHA – 24. července 2007</strong></p>
<p><strong>Pod hranici jednoho milionu řidičů se dostal počet těch, kteří dosud jezdí se starými řidičskými průkazy, které s koncem roku pozbudou platnosti. Ministerstvo dopravy ČR dnes představilo revidované statistiky, ze kterých byli vypuštěni již nežijící držitelé řidičských průkazů. Teplé letní dny a dovolené se sice negativně promítají do výměny starých řidičských průkazů, nicméně výměna i nadále pokračuje. Podle statistik, které má Ministerstvo dopravy k dispozici, stále ještě zbývá vyměnit více než 900 tisíc ŘP, vydaných v období mezi 1.7.1964 a 31.12.1993. Mírně si polepšila Praha, která se dostala pod hranici 200 tisíc starých ŘP, i přesto však ve statistikách zaujímá první místo, daleko za ní jsou Moravskoslezský kraj (přes 100 tisíc nevyměněných ŘP) a Jihomoravský kraj (přes 90 tisíc starých ŘP).</strong></p>
<p>K revidované statistice pomohlo propojení několika systémů, za jejichž pomoci byli z evidence řidičů odstraněni řidiči již mrtví. Díky tomuto kroku poklesl počet od nového roku neplatných dokladů o více než 200 tisíc na současných 900 tisíc. Z toho počtu je 40 % držitelů starých ŘP starších věku 60 let a lze proto předpokládat, že ne všichni si nechají nový ŘP vystavit. Podle Ministerstva dopravy tak, pokud se lidé budou chovat zodpovědně, lze do konce roku uskutečnit veškerou potřebnou výměnu&nbsp; starých ŘP.&nbsp;</p>
<p><em>„Problémem je, že lidé stále podceňují výměnu starých řidičských průkazů. Pokud ji oddalují, podstupují riziko, že ke konci roku budou stát na úřadech dlouhé fronty. Měli by si také uvědomit, že řízení s neplatným dokladem je přestupek, za který hrozí vysoké finanční postihy,“</em>říká Karel Hanzelka, tiskový mluvčí Ministerstva dopravy. <em>„V současné době by se mělo do konce roku 2007&nbsp; v průměru vyměnit téměř 40 tisíc řidičských průkazů týdně, aby výměna probíhala kontinuálně. To je 4krát více, než je týdně vyměněno dnes,“</em> dodává Hanzelka dále.</p>
<p>Právě současné horké letní dny a časté dovolené jsou důsledkem toho, že v posledních dnech počet vyměněných ŘP poklesl a nedosahuje ani průměrných hodnot předchozích týdnů. Lidé se obávají, že budou muset na úřadě trávit dlouhé hodiny čekáním a vyplňováním formulářů, což se jim v tropických vedrech nechce, nebo už jsou prostě u vody. <em>„Většina zodpovědných úřadů však vyšla občanům vstříc a je možné si telefonicky domluvit přesný termín a čas podání žádosti, která je k dispozici například na internetových stránkách Ministerstva dopravy. Ministerstvo dopravy se tak snaží, aby občané měli s touto výměnou pokud možno nejmenší možnou administrativní zátěž,“</em> říká Hanzelka.</p>
<p>Paradoxně se na úřady obrací velké množství lidí, kteří žádají o výměnu řidičského průkazu, ačkoli nemusí. <em>„Lidé někde slyší, že se musí vyměnit řidičské průkazy, ale už se nepodívají, zda ten jejich právě do této kategorie patří a rovnou si podají žádost o výměnu. To je však pro ně zbytečná administrativa a pro obce s rozšířenou působností další zátěž,“</em> říká tiskový mluvčí MD Karel Hanzelka. <em>„Doporučujeme, aby si všichni řidiči v České republice zkontrolovali své řidičské průkazy, zda nebyly vydány před rokem 1994, a&nbsp; pokud ano, aby neprodleně přistoupili k jejich výměně za nový typ. V opačném případně nebudou moci od nového roku řídit motorové vozidlo, nebo se budou dopouštět přestupku,“</em> varuje Hanzelka.</p>
