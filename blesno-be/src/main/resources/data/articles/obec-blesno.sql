      <p>Jako <strong>první zmínka o obci</strong> se uvádí rok 1496. Tehdy prodala Johanka z Březovic, dcera Přibíka Kroměšína z Březovic, pána třebechovické tvrze tuto tvrz i s dvorem a 11 obcemi a několik městeček k náležejících Mikuláši ml. Trčkovi z Lípy za 5 tis.grošů českých. Mezi vesnicemi se uvádí i Blešno, které se tak tímto obchodem dostalo pod panství opočenské. V roce 1634 byl syn majitele Adam Edrman zavražděn spolu s Albrechtem z Valdštejna a majitel panství Jan Rudolf Trčka z Lípy v témže roce zemřel. Panství opočenské bylo zkonfiskováno a v r.1636 je daroval císař Ferdinand II. bratřím Jeronýmu a Rudolfovi Colloredovým, kteří roku 1789 rozšířili sňatkem své jméno na Colloredo-Mandsfeld. Součástí panství opočenského byla obec až do roku 1849, kdy se stala obcí samostatnou.</p>
